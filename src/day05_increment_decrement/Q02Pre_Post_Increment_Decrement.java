package day05_increment_decrement;

public class Q02Pre_Post_Increment_Decrement {
    public static void main(String[] args) {
        int num1=9;
        int num2=num1++;
        int num3=--num2;

        num1++;
        --num2;

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("num3 = " + num3);





    }
}
