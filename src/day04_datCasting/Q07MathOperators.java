package day04_datCasting;

public class Q07MathOperators {
    public static void main(String[] args) {
        int sayi1 = 10;

        int sayi2 = 20;

        int temp;

        temp = sayi1;
        sayi1 = sayi2;
        sayi2 = temp;

        System.out.println("sayi1 = " + sayi1);
        System.out.println("sayi2 = " + sayi2);


    }
}
